# --------------------------------------------------------------------------- #
# Varon et al. 2018 IME / U_eff parameterization
# --------------------------------------------------------------------------- #
# Effective wind speed for the IME emission rate:
#     U_eff = ALPHA_1 * ln(U10) + ALPHA_2     [m/s, natural log]
# Coefficients are from Varon et al. 2018, AMT 11, 5673-5686, Eq. (12) Fig. 4.
# ALPHA_1 is reported as 0.9-1.1 across 1%-5% instrument precision; we take the
# midpoint as a single value with the spread propagated as uncertainty. ALPHA_2
# is a fixed 0.6 m/s in the published fit. The parameterization was derived
# from WRF-LES of GHGSat-class (50x50 m, 1-5% precision) observations and
# applied here to EMIT (60x60 m); this transfer is the dominant model-form
# uncertainty in our flux estimate. NEVER tune these to NASA's 18.3 t/hr.
#
# Source: Varon et al. 2018, doi:10.5194/amt-11-5673-2018 (Eq. 12).
VARON2018_ALPHA_1: float = 1.0
VARON2018_ALPHA_1_UNCERTAINTY: float = 0.1  # half-range of 0.9-1.1
VARON2018_ALPHA_2: float = 0.6  # m/s
VARON2018_U10_MIN_VALID_MS: float = 2.0  # below this, parameterization degrades


