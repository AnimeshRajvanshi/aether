# --------------------------------------------------------------------------- #
# Matched-filter output unit conversion
# --------------------------------------------------------------------------- #
# NASA's per-granule unit absorption spectrum was generated by MODTRAN with a
# specific Δc perturbation (the published EMIT target file numerically encodes
# k * Δc rather than k itself). The matched filter's raw α is therefore in
# units of Δc, not ppm·m. The conversion factor is published as the
# `--ppm_scaling` flag in NASA's operational matched filter, with default value
# 100000.0. Multiplying the raw MF output by this constant yields ppm·m.
#
# IMPORTANT: this constant is specific to NASA's per-granule target file
# generated for granule 20220815T042838 (and other EMIT-Data-Resources
# targets that share its MODTRAN Δc convention). If we ever generate k
# ourselves from HITRAN cross-sections (Sprint 3+ scope), this constant
# must be re-derived from our own Δc choice — likely 1.0 if we calibrate
# k directly in 1/(ppm·m).
#
# Source: NASA emit-sds/emit-ghg/parallel_mf.py:
#   parser.add_argument('--ppm_scaling', type=float, default=100000.0,
#                       help='scaling factor to unit convert outputs - based on target')
#   ...
#   mf[col, no_radiance_mask] = mf_col * args.ppm_scaling
MF_PPM_SCALING_FACTOR: float = 100000.0


# --------------------------------------------------------------------------- #
# Matched-filter spectral windows — narrow CH4 window
# --------------------------------------------------------------------------- #
# We use the narrow SWIR methane window 2137-2493 nm as the active band set
# for the matched filter. This is the gas-specific default in NASA's inner
# matched-filter routine `parallel_mf.py`:
#
#   if 'ch4' in args.library:
#       args.wavelength_range = [2137, 2493]
#
# *Why not the 3-window union (500-1340, 1500-1790, 1950-2450 nm)?*
# That broader set is the default in the higher-level NASA wrapper
# `ghg_process.py`, which is documented in EMIT GHG ATBD v0.2 §4.2.3. The
# broader set keeps ~219 EMIT bands; with only ~1280 column samples to
# estimate a 219x219 covariance, the inverse is highly underdetermined and
# the matched filter produces spurious bright artifacts unless stabilised by
# Monte Carlo bagging (NASA: --n_mc=10, --mc_bag_fraction=0.7) and dominant
# principal-component removal (--remove_dominant_pcs). Reproducing that
# stabilisation machinery is out of Sprint 2 scope.
#
# The narrow CH4 window (~49 EMIT bands) gives a well-conditioned 49x49
# covariance from the same 1280 samples, achieves correct output magnitudes
# without bagging, and is what the inner NASA matched-filter routine itself
# defaults to when invoked directly. It contains the entire SWIR methane
# absorption complex (the 2-nu3 band at 2.3 μm and shoulders).
#
# Sources:
# - EMIT-SDS operational code: github.com/emit-sds/emit-ghg, parallel_mf.py:
#     if 'ch4' in args.library: args.wavelength_range = [2137, 2493]
# - EMIT GHG ATBD v0.2 (Apr 2025), §4.2.3 (JPL D-107866) — describes the
#   3-window union used by the ghg_process.py wrapper with bagging.
MF_SPECTRAL_WINDOWS_NM: tuple[tuple[float, float], ...] = (
    (2137.0, 2493.0),
)


# --------------------------------------------------------------------------- #
# Covariance shrinkage — fixed diagonal-loading α (default)
# --------------------------------------------------------------------------- #
# Every per-column covariance is regularised by diagonal-loading shrinkage:
#     C_shrunk = (1 - α) S + α diag(S),       S = empirical sample covariance.
# We use a fixed α = 1e-9 per the EMIT GHG ATBD §4.2.1 ("a simple shrinkage
# method ... with a = 1e-9"). This gives the validated Stage A configuration:
# bbox Pearson ≈ 0.75 against NASA's L2B CH4ENH for the Goturdepe granule.
#
# **LOOCV-fit shrinkage is available behind an opt-in but is NOT the default.**
# NASA's operational `parallel_mf.py` uses LOOCV (Theiler 2012 closed form);
# we ported it verbatim (bit-identical alpha selection vs NASA's
# implementation on real EMIT columns — see scripts/diagnose_loocv_centering.py).
# When applied to uncentered EMIT radiance — which is how NASA's flow calls
# the function — LOOCV picks a median α ≈ 3e-4 with a mean-Mahalanobis bias
# that:
#   1. degrades the bulk-scene Pearson (0.75 → 0.64 measured)
#   2. does NOT preferentially regularise the blob-producing columns
#      (blob columns get α below the bulk median)
#   3. does NOT remove the round-blob artefacts at all (+2-8% blob mass)
# To opt in for an experiment, pass ``shrinkage_alpha="loocv"`` to
# ``run_matched_filter``. The function :func:`fit_looshrinkage_alpha` remains
# importable for diagnostic use.
#
# Sources:
# - EMIT GHG ATBD v0.2 §4.2.1 (JPL D-107866): a fixed α = 1e-9.
# - NASA emit-sds/emit-ghg/parallel_mf.py::fit_looshrinkage_alpha (LOOCV;
#   our port lives in matched_filter.py for diagnostic use only).
# - Theiler, "The Incredible Shrinking Covariance Estimator", Proc. SPIE,
#   2012, Eq. 29 — closed-form LOOCV objective.
MF_SHRINKAGE_ALPHA: float = 1e-9

# LOOCV grid + numerical-stability scaling — used only when the caller opts
# in via ``shrinkage_alpha="loocv"``.
MF_LOOCV_ALPHA_LOG10_MIN: float = -10.0
MF_LOOCV_ALPHA_LOG10_MAX: float = 0.0
MF_LOOCV_ALPHA_LOG10_STEP: float = 0.05
MF_LOOCV_STABILITY_SCALING: float = 100.0


